//Q1. What are conditional statements? Explain with syntax and examples.
//Answer:
//Conditional statements control the flow of a program based on a condition.
//Syntax & Example:

let marks = 73;

if (marks > 90) {
  console.log("A Grade");
} else if (marks >= 70) {
  console.log("B Grade");
} else if (marks >= 50) {
  console.log("C Grade");
} else {
  console.log("F Grade");
}

