/*Q3. What are loops and why do we need them? Explain types with syntax and examples.
Answer:
Loops are used to repeat a block of code multiple times.

Types:*/

// For Loop:

for (let i = 1; i <= 5; i++) {
  console.log(i);
}

// While Loop:

let i = 1;
while (i <= 5) {
  console.log(i);
  i++;
}

// Do-While Loop:

let j = 1;
do {
  console.log(j);
  j++;
} while (j <= 5);
